package com.buaa.fly.sfstatistic.vo;

public class SFscheme {
	private Integer month;
	private Integer plandays;
	private Integer factdays;
	private Integer planjiaci;
	private Integer factjiaci;
	
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	public Integer getPlandays() {
		return plandays;
	}
	public void setPlandays(Integer plandays) {
		this.plandays = plandays;
	}
	public Integer getFactdays() {
		return factdays;
	}
	public void setFactdays(Integer factdays) {
		this.factdays = factdays;
	}
	public Integer getPlanjiaci() {
		return planjiaci;
	}
	public void setPlanjiaci(Integer planjiaci) {
		this.planjiaci = planjiaci;
	}
	public Integer getFactjiaci() {
		return factjiaci;
	}
	public void setFactjiaci(Integer factjiaci) {
		this.factjiaci = factjiaci;
	}
}

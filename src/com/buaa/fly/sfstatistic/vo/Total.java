package com.buaa.fly.sfstatistic.vo;

public class Total {
	private String count;
    private String time;
	private String thisyearcount;
    private String thisyeartime;
    
    public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getThisyearcount() {
		return thisyearcount;
	}
	public void setThisyearcount(String thisyearcount) {
		this.thisyearcount = thisyearcount;
	}
	public String getThisyeartime() {
		return thisyeartime;
	}
	public void setThisyeartime(String thisyeartime) {
		this.thisyeartime = thisyeartime;
	}
}
